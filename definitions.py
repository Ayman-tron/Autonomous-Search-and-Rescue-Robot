
# Pins for Motor Control

# Enabler pin A and B of the L293D motor

# Motor #1, PWM Pin 29
ENA_PIN = 29
# Motor #2, PWM Pin 31
ENB_PIN = 31

# Pin 11 and 12 allow us to control the direction of the Motor 1
IN1 = 11
IN2 = 12

# Pin 13 and 15 allow us to control the direction of the Motor 1
IN3 = 13
IN4 = 15

# Pins used to configure the UltraSonic Sensor on the right side of the robot
TRIG_PIN_A = 16
ECHO_PIN_A = 18

# Pins used to configure the UltraSonic Sensor on the Left side of the robot
TRIG_PIN_B = 38
ECHO_PIN_B = 40

# Pins used to configure the UltraSonic Sensor on the Front side of the robot
TRIG_PIN_C = 35
ECHO_PIN_C = 37

# Pin that reads from the IR sensor to tell us whether an obstacle is present or not
IR_PIN = 21
